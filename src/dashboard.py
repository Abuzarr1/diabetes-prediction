import streamlit as st
import pandas as pd
import numpy as np
import joblib
import os

# Set page configuration
st.set_page_config(
    page_title="Diabetes Risk Simulator",
    page_icon="🏥",
    layout="wide",
    initial_sidebar_state="expanded"
)

# ---------------------------------------------------------
# MODEL & PREPROCESSOR LOADING
# ---------------------------------------------------------

@st.cache_resource
def load_ml_assets():
    model_path = "models/diabetes_rf_model.pkl"
    scaler_path = "models/scaler.pkl"
    columns_path = "models/model_columns.pkl"
    
    if os.path.exists(model_path) and os.path.exists(scaler_path):
        model = joblib.load(model_path)
        scaler = joblib.load(scaler_path)
        model_columns = joblib.load(columns_path)
        return model, scaler, model_columns
    return None, None, None

model, scaler, model_columns = load_ml_assets()

# ---------------------------------------------------------
# APPLICATION RENDER LAYOUT
# ---------------------------------------------------------

st.title("🏥 Enterprise Diabetes Risk Prediction")
st.markdown("Developed by **Data Science Intern** — A clinical decision support system powered by Random Forest ML.")
st.markdown("---")

if model is None:
    st.error("🚨 Models not found. Please run `src/train.py` first.")
else:
    # Build a professional two-column layout
    col_input, col_output = st.columns([1.2, 1])

    with col_input:
        st.subheader("🩺 Patient Clinical Inputs")
        
        # We group inputs into expanders or visually group them
        st.markdown("#### Patient Vitals")
        col1, col2, col3 = st.columns(3)
        with col1:
            weight = st.slider("Weight (kg)", 40.0, 150.0, 75.0, 0.5)
        with col2:
            waist = st.slider("Waist Size (inches)", 24.0, 60.0, 36.0, 0.5)
        with col3:
            exercise = st.slider("Exercise (mins/week)", 0, 400, 150, 10)
            
        st.markdown("#### Blood Pressure")
        col4, col5 = st.columns(2)
        with col4:
            sys_bp = st.slider("Systolic BP (mmHg)", 90, 200, 120, 1)
        with col5:
            dia_bp = st.slider("Diastolic BP (mmHg)", 60, 120, 80, 1)
            
        st.markdown("#### Lab Results")
        col6, col7 = st.columns(2)
        with col6:
            hba1c = st.slider("HbA1c Level (%)", 4.0, 14.0, 5.5, 0.1)
        with col7:
            blood_sugar = st.slider("Fasting Blood Sugar (mg/dL)", 70, 400, 95, 1)
            
        st.markdown("#### Medical History & Symptoms")
        col8, col9, col10 = st.columns(3)
        with col8:
            family_hist = st.checkbox("Family History of Diabetes")
            polydipsia = st.checkbox("Polydipsia (Excessive Thirst)")
            polyuria = st.checkbox("Polyuria (Excessive Urination)")
        with col9:
            vision = st.checkbox("Vision Issues")
            nephropathy = st.checkbox("Nephropathy (Kidney Disease)")
        with col10:
            duration = st.slider("Diabetes Duration (Years)", 0, 30, 0, 1)
            
        region = st.selectbox("Patient Region", ["Punjab", "Sindh", "KPK", "Balochistan"])

    with col_output:
        st.subheader("🔮 ML Inference Engine")
        st.markdown("Real-time clinical inference generated by the Random Forest classifier:")
        
        # Build the input dictionary
        input_data = {
            'Weight': weight,
            'WaistSize': waist,
            'SysBP': sys_bp,
            'DiaBP': dia_bp,
            'FamilyHistory': 1 if family_hist else 0,
            'HbA1c': hba1c,
            'BloodSugar': blood_sugar,
            'VisionIssue': 1 if vision else 0,
            'Exercise': exercise,
            'Polydipsia': 1 if polydipsia else 0,
            'Polyuria': 1 if polyuria else 0,
            'DiabetesDuration': duration,
            'Nephropathy': 1 if nephropathy else 0,
            'Region': region
        }
        
        # Convert to DataFrame
        df_input = pd.DataFrame([input_data])
        
        # One-hot encode the Region column matching the training pipeline
        # First create dummy columns for region
        df_input = pd.get_dummies(df_input, columns=['Region'])
        
        # Ensure all columns from the training set are present
        for col in model_columns:
            if col not in df_input.columns:
                df_input[col] = 0
                
        # Enforce exact column order
        df_input = df_input[model_columns]
        
        # Scale the features
        X_scaled = scaler.transform(df_input)
        
        # Run prediction
        prob = model.predict_proba(X_scaled)[0, 1]
        
        st.markdown("---")
        st.markdown(f"## 🩸 Patient Diabetes Risk: `{prob:.1%}`")
        
        if prob >= 0.65:
            st.error("🔴 **Classification: HIGH CLINICAL RISK**\n\nPatient exhibits critical biomarkers consistent with Diabetes. Recommend immediate HbA1c verification and endocrinology consult.")
        elif prob >= 0.35:
            st.warning("🟡 **Classification: PRE-DIABETIC / ELEVATED RISK**\n\nPatient displays warning signs. Recommend lifestyle intervention, increased exercise, and dietary adjustments.")
        else:
            st.success("🟢 **Classification: LOW RISK**\n\nPatient vitals are currently within healthy clinical parameters.")
            
        st.markdown("---")
        st.markdown("### 🧬 Feature Importance Insight")
        st.info("The Random Forest model primarily relies on **HbA1c**, **Fasting Blood Sugar**, and **Waist Size** to make this determination.")
